print("O resultado da adição é:", SOMA)

SUBTRACAO = X - Y
print("O resultado da subtração é:", SUBTRACAO)

MULTIPLICACAO = X * Y
print("O resultado da multiplicação é:", MULTIPLICACAO)

DIVISAO = X / Y
print("O resultado da divisão é:",DIVISAO)

